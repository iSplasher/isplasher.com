<h2 class="title2" style="top: 1px">Opgave i kostens sammensætning</h2>
		<pre style="font-size: 15px; font-family: "Quicksand"; background-color: #FFF">
1)
Kulhydrater – Bruges primært som energi. Der findes både monosakkarider, di-
sakkarider og polysakkarider. monosakkarider kan blive omdannet til energi 
allerede når det kommer ind i munden mens disakkarider og polysakkarider 
først skal nedbrydes ned til monosakkarider før de kan blive brugt som e-
nergi. Fedt – Bruges som energi og byggesten. Det bliver brugt i cellerne
til at lave cellemembranen. Fedt bliver også brugt som energilager og iso-
lation. Protein – Bruges som energi og opbygning af enzymer og muskler.
<h4 style="color: #F63">Illustration a kulhydrater:</h4>
<img src="http://www.bodyman.dk/images/new%20folder/kindofcarbs.jpg">

2)
Kulhydrater, monosakkarid kan findes i druesukker og frugtsukker. Disakka-
rider kan findes i normalt sukker. Polysakkarider kan findes i kartofler.
Fedt, mættet fedtstoffer kan findes i ost og dyrekød. Umættet fedtstoffer
kan findes mest i planter. Flerumættet er primært fisk. Protein, finder
man i planter og dyr.

3)
Kulhydrat – 17kj/g
Fedt – 38kj/g
Protein – 17kj/g

4)
Dette kan variere meget, siden der er mange forskellige variabler man skal
tage udgangspunk i. Det kan blandt andet være vægt, alder og køn. Neden-
stående har vi en liste over den gennemsnittelige energiforbrug mænd skal
have, i forskellige aldre.

Mænd på 18-30 år - 13.000 kJ pr. dag

Mænd på 31-60 år - 12.500 kJ pr. dag

Mænd på 61-74 år - 11.300 kJ pr. dag

Mænd på 75 år og derover - 10.200 kJ pr. dag


